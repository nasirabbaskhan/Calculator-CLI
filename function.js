export function sum(a, b) {
    return a + b;
}
export function subtract(a, b) {
    return a - b;
}
export function multiplication(a, b) {
    return a * b;
}
export function division(a, b) {
    return a / b;
}
export function modulus(a, b) {
    return a % b;
}
export function power(a, b) {
    return a ** b;
}
